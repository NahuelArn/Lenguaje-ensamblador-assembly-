Escribir un programa que solicite el ingreso de cinco caracteres por teclado y los almacene en memoria.
Una vez ingresados, que los envíe a la impresora a través del HAND-SHAKE, en primer lugar tal cual
fueron ingresados y a continuación en sentido inverso. Utilizar el HAND-SHAKE en modo consulta de
estado. ¿Qué diferencias encuentra con el ejercicio 2c?

;HAND-SHAKE
HAND_DATO EQU 40H
HAND_ESTADO EQU 41H


ORG 1000H
STRING DB 0,0,0,0,0
SALTO DB 0AH
FLAG DB 0
ORG 3000H


ORG 2000H
;CONFIGURAR EL HAND-SHAKE PARA EL POLING
MOV AL, HAND_ESTADO
AND AL, 07FH ;01111111B
OUT HAND_ESTADO, AL ;0XXXXXXX

;PEDIR CARACTERES
MOV DL, 5
MOV BX, OFFSET STRING
LOPARDO: CMP DL, 0
JZ PROSIGO
INT 6
INC BX
DEC DL
JMP LOPARDO

PROSIGO: NOP

MOV BX, OFFSET STRING
MOV DL, 5 ;CANTIDAD A IMPRIMIR
CMP FLAG, 1
JNZ POL
INVERSO: NOP
MOV DL, 6 ;CANTIDA EN ORDEN INVERSO
MOV BX, OFFSET SALTO
;HAGO EL PULLING PARA VER SI MI IMPRESORA ESTA OCUPADA
POL: NOP
IN AL, HAND_ESTADO  ;ME TRAIGO EL VALOR DEL ESTADO
AND AL, 1
JNZ POL
;MANDO CARACTER A LA IMPRESORA
MOV AL, [BX]
OUT HAND_DATO, AL
CMP FLAG, 1
JNZ NORMAL
DEC BX
DEC DL
JNZ POL
JMP LAZO
NORMAL: INC BX
DEC DL
JNZ POL
INC FLAG
JMP INVERSO 

LAZO: JMP LAZO
INT 0
END