Escribir un programa que efectúe la resta de dos números (de un dígito cada uno) ingresados por teclado y muestre el
resultado en la pantalla de comandos. Antes de visualizarlo el programa debe verificar si el resultado es positivo o negativo
y anteponer al valor el signo correspondiente.

ORG 1000H                           ;VAR GLB
MSJ DB "INGRESE UN NUMERO: "
FIN DB 00H

ORG 1500H                           ; UTILIDADES
NUM1 DB ?
NUM2 DB ?

SIGNO DB "-"               ;NEGATIVO
RESULTADO DB ?

SIGN DB "+"                ;POSITIVO
RESULTAD DB ?        

ORG 3000H

NEGATIVO: SUB AH, AL ;AL REVES EN NEGATIVO
MOV AL, AH
ADD AL, 30H
MOV BX, OFFSET RESULTADO
MOV [BX], AL                      ;NEGATIVO
;ME POSICIONO PARA IMPRIMIR             
MOV BX, OFFSET SIGNO
MOV AL, 2
INT 7

RET

POSITIVO: SUB AL, AH
ADD AL, 30H
MOV BX, OFFSET RESULTAD
MOV [BX], AL                      ;POSITIVO
; ME POSICIONO PARA IMPRIMIR
MOV BX, OFFSET SIGN
MOV AL, 2
INT 7

RET

PEDIRNUM: NOP

MOV BX, OFFSET NUM1
INT 6

MOV BX, OFFSET NUM2
INT 6

MOV AL, NUM1 ;AL SE QUEDA CON NUM1
MOV AH, NUM2 ;AH SE QUEDA CON NUM2
;AHORA SACO UNO A DECIMAL Y EL OTRO DEL DEJO EN ASSCI

SUB AL, 30H ; AL LO TENGO EN SU VALOR DECIMAL
SUB AH, 30H ;AH LO TENGO EN DECIMAL

CMP AL, AH ; NUM1 - NUM2 Y LO GUARDO EN AL
JS NEGATIVO
JNS POSITIVO

RET


ORG 2000H
MOV BX, OFFSET MSJ
MOV AL, OFFSET FIN - OFFSET MSJ
INT 7 ;INGRESE UN NUMERO

PUSH AX
PUSH BX
PUSH CX       ;SE PUSHEA SI USAS LOS VALORES REGISTROS EN LAS SUBRUTINAS PARA NO PERDER EL VALOR EN EL QUE ESTABAN
PUSH DX       ; EN EL PROGRAM PRINCIPAL

CALL PEDIRNUM ;PIDO MIS 2 NUMEROS A RESTAR

POP DX 
POP CX
POP BX         ;PUMPEO? XD {RECUPERO MIS REGISTROS CON SU CONTENDIDO DEL PROGRAM PRINCIPAL}
POP AX

INT 0
END