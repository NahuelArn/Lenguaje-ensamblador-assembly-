Escribir un programa que solicite el ingreso de un número (de un dígito) por teclado y muestre en pantalla dicho
número expresado en letras. Luego que solicite el ingreso de otro y así sucesivamente. Se debe finalizar la ejecución al
ingresarse en dos vueltas consecutivas el número cero.

ORG 1000H
 NUM0 DB "CERO    " 
 NUM1 DB "UNO     "
 NUM2 DB "DOS     "
 NUM3 DB "TRES    "
 NUM4 DB "CUATRO  "
 NUM5 DB "CINCO   "
 NUM6 DB "SEIS    "
 NUM7 DB "SIETE   "
 NUM8 DB "OCHO    "
 NUM9 DB "NUEVE   " 
 FIN DB 0
 NO_ES_UN_NUMERO DB "EL CARACTER INGRESADO NO ES UN NUMERO"
 INGRESE_UN_NUMERO DB "INGRESE UN NUMERO: "
 NUM_INGRESO DB ?
 ESPACIO DB 0DH       ;SALTO DE LINEA
 
 
ORG 3000H

 MULTIPLICACION: SUB CL, 30H ;RESTO CONVIERTO EL ASSCI A DECIMAL
 LOOP: CMP CL, 0 ;VEO QUE NO SEA 0, CUANDO LLEGA A 0 YA TENGO LA CANTIDAD A MULTIPLICAR
 JZ VUELVO
 
 ADD DX, 8  ; EN DL VOY A SUMAR "CL" VECES EL 8 PARA SACAR SU MULTIPLIACION
 DEC CL ;DECREMENTO EN UNO EL LOOP
 JMP LOOP
 VUELVO: RET
 
 IMPRIMIR: MOV CL, [BX] ;PONGO EN CEL EL VALOR QUE LEI
 MOV BX, OFFSET NUM0
 CALL MULTIPLICACION 
 ADD BX,  DX ; TENGO SUMA LA CANTIDAD DE VECES Q VA MULTIPLCIAR LA DIREC BASE  ; 8 uno / 16 dos / 32 
 MOV AL, 8 ;A
 INT 7     ;
 
 JMP VUELVO_ALPROGRAM_PRINCIPAL
 
 SOLICITAR: NOP
 ;MOV BX, INGRESE_UN_NUMERO ;TIRO MI MENSAJE "INGRESE UN NUMERO"
 ;MOV AL, OFFSET NUM_INGRESO- OFFSET INGRESE_UN_NUMERO             ; LO MOVI AL PROGRAM PRINCIPAL
 ;INT 7 ;IMPRIMO
 
 MOV BX, OFFSET NUM_INGRESO
 INT 6
 CMP BYTE PTR [BX], 30H  ;0
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 31H  ;1
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 32H  ;2
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 33H  ;3
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 34H  ;4
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 35H  ;5
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 36H  ;6
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 37H  ;7
 JZ IMPRIMIR             
 CMP BYTE PTR [BX], 38H  ;8
 JZ IMPRIMIR
 CMP BYTE PTR [BX], 39H  ;9
 JZ IMPRIMIR
 
 MOV BX, OFFSET NO_ES_UN_NUMERO
 MOV AL, OFFSET INGRESE_UN_NUMERO- OFFSET NO_ES_UN_NUMERO ; SI NO ES UN NUMERO VUELVO A PEDIR 

 INT 7
 JMP SOLICITAR ; EL CARACTER QUE INGRESO NO ERA UN NUMERO, VUELVE A PEDIRLE UN NUMERO
 VUELVO_ALPROGRAM_PRINCIPAL: RET
 
ORG 2000H
 MOV BX, OFFSET INGRESE_UN_NUMERO ;TIRO MI MENSAJE "INGRESE UN NUMERO"
 MOV AL, OFFSET NUM_INGRESO- OFFSET INGRESE_UN_NUMERO
 INT 7 ;IMPRIMO
 
 MOV BX, OFFSET ESPACIO
 MOV AL, 1                   ;ESPACIO/ SALTO DE LINEA, MEJORAR {HACER UNA SUBRUTINA PARA HACER LOS SALTOS DE LINEA Q QUIERA}
 INT 7
 
 PUSH AX
 PUSH BX
 PUSH CX
 PUSH DX
 CALL SOLICITAR
 
INT 0
END