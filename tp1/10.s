CON PILA


ORG 1000h
 DATO1 DW 2 ;DATO1
 DATO2 DW 5 ;DATO2

ORG 3000h
SWAP: MOV BX, SP

ADD BX, 2
MOV AX, WORD PTR [BX] ;GUARDO EN AX MI DATO2
ADD BX, 2
MOV CX, WORD PTR [BX] ;GUARDO EN CX MI DATO1

;USO DE DIRECCIONES

;MANDO EL 5
MOV BX, AX
MOV AX, [BX]
PUSH AX

;MANDO EL 2
MOV BX, CX
MOV CX, [BX]
PUSH CX

;ME ACOMODO
MOV BX, SP

;INTERCAMBIO

;PRIMER CAMBIO
MOV CX, [BX] ;PONGO EN CX EL 2
ADD BX, 6
MOV DX, [BX] ;PONGO EN DX LA DIREC DE DATO2
; HAGO EL PRIMER TRUEQUE
MOV BX, DX ; CARGO EL PUNTERO CON LA DIREC DE DATO2
MOV [BX], CX ; CAMBIO REALIZADO

;SEGUNDO CAMBIO

;ME ACOMODOX2=
MOV BX, SP
ADD BX, 2
MOV CX, [BX] ;PONGO EN CX EL 5
ADD BX, 6
MOV DX, [BX] ; PONGO EN DX LA DIREC DE DATO1
;HAGO EL SEGUNDO RULO
MOV BX, DX  ;CARGO EL PUNTERO CON LA DIREC DE DATO1
MOV [BX], CX ;CAMBIO REALIZADO

POP AX
POP CX

RET
ORG 2000h
 
 MOV AX, OFFSET DATO1
 PUSH AX
 MOV AX, OFFSET DATO2
 PUSH AX
 CALL SWAP
 POP AX
 POP BX
HLT
END




