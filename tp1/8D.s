ORG 1000H
 CADENA DB "AEE"
 FIN_CADENA DW 00
 ESVOC DW 0FFh
 NOES DW 00h
 RESULT DW ?
 CONTV DW ?
 
ORG 3000H
 
 INCRE: INC DL
 JMP SIGO
 
 CONTROLADOR: MOV DL, 0 ;CONTADOR DE VOCALES
 LOOP: CMP BYTE PTR [BX], 00H ;VEO SI NO ES 0
 JZ TERMINO                 ;TERMINA
 
 MOV AH, BYTE PTR [BX]  ; PONGO LA VOCAL ACTUAL
 CALL ES_VOCAL
 CMP AL, ESVOC
 JZ INCRE
 
 SIGO:INC BX
 JMP LOOP
 TERMINO: RET
 
 ES_VOCAL: MOV AL, ESVOC
 CMP AH, 41h ; A
 JZ FIN
 CMP AH, 45h ; E
 JZ FIN
 CMP AH, 49H ; I
 JZ FIN
 CMP AH, 4FH ; O
 JZ FIN
 CMP AH, 55H ; U
 JZ FIN
 CMP AH, 61h ; a
 JZ FIN
 CMP AH, 65h ; e
 JZ FIN
 CMP AH, 69H ; i
 JZ FIN
 CMP AH, 6FH  ; o
 JZ FIN
 CMP AH, 75H ; u
 JZ FIN
 MOV AL, NOES ; NO ES 
 FIN: RET

ORG 2000h
 MOV BX, OFFSET CADENA ;1000h
 ;MOV BX, OFFSET CONTV
 CALL CONTROLADOR
 MOV RESULT, AL
 MOV CONTV, DL
 HLT
END